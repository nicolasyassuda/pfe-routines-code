void oled_draw_char(int fd, int page, int column, char c) {
    const uint8_t *chars = &font5x7[(int)(c - 32)][0];

    oled_send_byte(fd, 0xB0 + page, OLED_CMD_MODE);          // Page
    oled_send_byte(fd, 0x00 + (column & 0x0F), OLED_CMD_MODE); // Low nibble of column
    oled_send_byte(fd, 0x10 + ((column >> 4) & 0x0F), OLED_CMD_MODE); // High nibble of column

    for (int i = 0; i < 5; i++) {
        oled_send_byte(fd, chars[i], OLED_DATA_MODE);
    }

    oled_send_byte(fd, 0x00, OLED_DATA_MODE); // Espaço entre letras
}

void oled_draw_string(int fd, int page, int column, const char *str) {
    while (*str) {
        oled_draw_char(fd, page, column, *str++);
        column += 6; // 5 cols para caractere + 1 espaço
    }
}